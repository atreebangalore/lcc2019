<p style="margin: 5px 5px 5px 5px;">
Lantana is one of the world’s most invasive weeds. It is used as a substitute for native species such as bamboo and cane and strengthening the livelihood of Soliga tribal community. No-cost and simple skills are used to empower the community. ATREE introduced Lantana craft as a way of improving livelihoods through furniture and other utility products. This contributes conservation of native plant diversity in natural forest.<br><br>

<strong>Weeds to wealth approach:</strong> ATREE began developing lantana crafts in 2004, establishing the Lantana Craft Centres (LCCs), and has trained over 350 Soliga youth. More than 86 different lantana craft products have been developed in the past 18 years. 400 artisans have been trained at 28 locations in 5 states (Kerala, Andhra Pradesh, Tamil Nadu, Maharashtra, and Karnataka). This training programme has been requested by NGOs and the forest department. Till now 350 artisans in MM Hills have enrolled with the Handicraft Department and received ID cards, and benefits such as medical and life insurance. Lantana craft activity in MM Hills generated livelihoods it has benefited over 200 families. Lantana craft products are sold across India, in cities such as Bangalore, Chennai, Mumbai, and locally to pilgrims. <br><br>

<strong>Lantana impacts and monitoring:</strong> Baseline study related to the distribution of Lantana camara and its impact on local biodiversity led to the development of a proposal that successfully created Lantana craft initiatives in MM Hills in 2003. Lantana harvest impacts studies have indicated that Lantana density has decreased significantly around the villages where artisans harvested actively. This initiative has contributed to the regeneration of native species. It also helped to control forest fires, especially canopy fires that have been well observed by the local community and documented by the MM Hills research team. The Lantana craft initiative has immense implications for the control and management of lantana in MM Hills sanctuary. <br><br>

