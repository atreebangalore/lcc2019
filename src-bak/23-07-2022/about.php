<h3>Mission: Strengthening/ Sustaining the Lantana Craft Center (LCC) by customizing products for rural and urban markets</h3>
<p style="text-align:left;margin: 5px 5px 5px 5px;">
<strong>Background:</strong> Lantana is one of the world’s most invasive weeds. It is used as a substitute for native species such as bamboo and cane and strengthening the livelihood of Soliga tribal community. No-cost and simple skills are used to empower the community. ATREE introduced Lantana craft as a way of improving livelihoods through furniture and other utility products. This contributes conservation of native plant diversity in natural forest.<br><br>

Product diversification: Simple technique of bending Lantana stems, which made it possible to make a range of products like baskets, furniture, toys, utilities, ornaments, and life-size elephants.  <br><br>

Till now: ATREE has trained over 650 people in lantana craft, 40% of these folk are women. Trained artisans derive nearly 80% of their cash income from lantana crafts. The average income of an artisan ranges from Rs 12500 – 16000. Health cards and insurance coverage provided for 360 artisans. ATREE disseminates technology and craft to NGOs and 7 forest communities in 23 forest areas of  Karnataka, Tamil Nadu, Andhra Pradesh, Kerala, and Maharashtra. <br><br>

